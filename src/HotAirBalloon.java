public abstract class HotAirBalloon {
//     It has the liftUp method that displays that the balloon lifts up.
    public void liftUp() {
        System.out.println("The balloon lifts up.");
    }
//      It has the land method that just displays that the balloon lands.

    public void land() {
        System.out.println("The balloon lands.");
    }

}
